
DATO EQU 0X21

INICIO

	ORG	0X00
	GOTO	START

START

	BSF	STATUS, 5
	CLRF	TRISB
	BSF	TRISD, 7
	BCF	STATUS, RP0

	MOVLW	0X00
	MOVWF	PORTB

MENU
	BTFSC	PORTD,7
	GOTO 	DERECHA
	GOTO	IZQUIERDA	

	;CALL 	MV01
	;CALL	MV02
	GOTO 	MENU

DERECHA
	CALL	MV1
	CALL	MV2
	GOTO	MENU

IZQUIERDA
	CALL	MV3
	CALL	MV4
	GOTO	MENU


MV
	MOVF	DATO,W
	MOVWF	PORTB
	RETURN
	

MV1
	MOVLW	B'00000001'
	MOVWF	DATO
	CALL	MV
	RETURN

MV2
	MOVLW	B'00000010'
	MOVWF	DATO
	CALL	MV
	RETURN
MV3
	MOVLW	B'10000000'
	MOVWF	DATO
	CALL	MV
	RETURN

MV4
	MOVLW	B'01000000'
	MOVWF	DATO
	CALL	MV
	RETURN
END